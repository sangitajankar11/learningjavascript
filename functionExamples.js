function greet(name) {              //name is a parameter that will receive the value passed when the function is called
    return "Hello, " + name + "!";       //The function returns a string that combines the greeting "Hello, " with the value of the 'name' parameter and an exclamation mark.
}
 

console.log(greet("Alice"));  // Output: Hello, Alice! -->Alice is the argument passed to the function, and it will be used to replace the parameter 'name' in the function body. The function will return a greeting message that includes the name provided as an argument.
